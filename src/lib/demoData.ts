export function getCustomersForUser(userId: string): Customer[] {
  return DEMO_CUSTOMERS.filter(
    (c) => c.assigned_to === userId || c.created_by === userId
  )
}

export function getProjectsForUser(userId: string): Project[] {
  return DEMO_PROJECTS.filter(
    (p) => p.assigned_to === userId || p.created_by === userId
  )
}

export function getFollowupsForProject(projectId: string): Followup[] {
  return DEMO_FOLLOWUPS.filter((f) => f.project_id === projectId)
}

export function getQuotesForUser(userId: string): Quote[] {
  const projects = getProjectsForUser(userId)
  const projectIds = projects.map((p) => p.id)
  return DEMO_QUOTES.filter((q) => projectIds.includes(q.project_id))
}

export function getQuoteItems(quoteId: string): QuoteItem[] {
  return DEMO_QUOTE_ITEMS.filter((q) => q.quote_id === quoteId)
}

export function getPaymentsForUser(userId: string): Payment[] {
  const projects = getProjectsForUser(userId)
  const projectIds = projects.map((p) => p.id)
  return DEMO_PAYMENTS.filter((p) => p.project_id && projectIds.includes(p.project_id))
}

export function getKPIForUser(userId: string, period: string): KPI[] {
  return DEMO_KPIS.filter(
    (k) => k.employee_id === userId && k.period === period
  )
}

